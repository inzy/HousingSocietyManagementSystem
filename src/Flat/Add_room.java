    private void floor_nextMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_floor_nextMouseClicked
        // TODO add your handling code here:
        Matcher num = Others.Constant.NUM.matcher(jTextField2.getText()+jTextField3.getText());
        String str = jTextPane2.getText();
        if(!"".equals(jTextField2.getText()) && !"".equals(jTextField3.getText())) {
            if(num.matches()) {
                jTextPane2.setText(str+jTextField2.getText().toUpperCase()+" - "+jTextField3.getText().toUpperCase()+"\n");
                for(int i = Integer.parseInt(jTextField2.getText()); i <= Integer.parseInt(jTextField3.getText());i++) {
                    if(i < 10){
                        room.add("00"+i+"");
                    } else if (i < 100) {
                        room.add("0"+i+"");
                    } else {
                        room.add(+i+"");
                    }
                }
                floor++;
                jLabel14.setText(floor+"");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField2.requestFocus();
            } else {
                JOptionPane.showMessageDialog(rootPane,"Both values must be numeric!");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField2.requestFocus();
            }
        } else {
                JOptionPane.showMessageDialog(rootPane,"Enter range of room number!");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField2.requestFocus();
        }
    }//GEN-LAST:event_floor_nextMouseClicked

