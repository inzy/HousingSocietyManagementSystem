    private void wing_nextMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_wing_nextMouseClicked
        // TODO add your handling code here:
        Matcher alphanum = Others.Constant.ALPHANUM.matcher(jTextField1.getText());
        String str = jTextPane1.getText();
        if(!"".equals(jTextField1.getText())) {
            if(alphanum.matches()) {
                jTextPane1.setText(str+jTextField1.getText().toUpperCase()+"\n");
                wing.add(jTextField1.getText().toUpperCase());
                jTextField1.setText("");
                jTextField1.requestFocus();
            } else {
                JOptionPane.showMessageDialog(rootPane,"Wing name cannot contain special characters!");
                jTextField1.setText("");
                jTextField1.requestFocus();
            }
        } else {
            JOptionPane.showMessageDialog(rootPane,"Enter wing name!");
            jTextField1.setText("");
            jTextField1.requestFocus();
        }
    }//GEN-LAST:event_wing_nextMouseClicked

